package com.example.calculator;

public class Calculos {

    public double suma(double num1, double num2){return num1+num2;}
    public double resta(double num1, double num2){return num1-num2;}
    public double multi(double num1, double num2){return num1*num2;}
    public double div(double num1, double num2){return num1/num2;}
    public double pot(double num1, double num2){return Math.pow(num1,num2);}
}
